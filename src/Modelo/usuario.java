package Modelo;

/**
 *
 * @author Osvaldo
 */
public class usuario {
    String u;
    String c;

    public usuario(String u, String c) {
        this.u = u;
        this.c = c;
    }

    public String getU() {
        return u;
    }

    public void setU(String u) {
        this.u = u;
    }

    public String getC() {
        return c;
    }

    public void setC(String c) {
        this.c = c;
    }
    
}
